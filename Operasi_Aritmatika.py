# membuat operasi aritmatika
print ("====Operasi Aritmatika====")

a = int(input("masukan nilai pertama :"))
b = int(input("Masukan Nilai Kedua :"))
# Operasi Penjumlahan
hasil = a + b
print(a,'+',b,'=',hasil)

# Operasi Pengurangan 
hasil = a - b
print(a,'-',b,'=',hasil)

#Operasi Perkalian
hasil = a * b
print(a,'x',b,'=',hasil)

# operasi pembagian
hasil = a / b
print(a,'/',b,'=',hasil)

# Operasi Pangkat
hasil = a ** b
print(a,'**',b,'=',hasil)

